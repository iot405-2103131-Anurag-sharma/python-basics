# Integer
int = 10

# Boolean
bool = True

# Character (Python does not have a distinct 'char' type, we can use a single-character string)
char = 'a'

# Float
float = 3.14

# Double (In Python, there is no distinction between float and double)
double = 3.14159

# Printing the variables
print("Integer:", int)
print("Boolean:", bool)
print("Character:", char)
print("Float:", float)
print("Double:", double)
